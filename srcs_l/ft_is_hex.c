#include "libft.h"

int				ft_is_hex(char c)
{
	if (c == 'A' || c == 'B' || c == 'C' || c == 'D' || c == 'E' || c == 'F' ||
		c == 'a' || c == 'b' || c == 'c' || c == 'd' || c == 'e' || c == 'f')
		return (1);
	else
		return (0);
}